def extract_number(s: str) -> Optional[int]:
    # Remove commas from the text
    text = s.replace(',', '')

    # Define a regular expression pattern to find integer values
    pattern = r'\b(\d+)\b'

    # Find the first match in the text
    match = re.search(pattern, text)

    # Extract the integer value
    if match:
        value = int(match.group(1))
    else:
        # Handle the case where no match is found (optional)
        value = None

    return value

def extract_weights(s: str) -> Optional[Tuple[int, int]]:
    # Define a regular expression pattern to find integer values with various separators
    text = s.replace(",","")
    pattern = r'\b(\d+)'

    # Find all matches in the text
    matches = re.findall(pattern, text)

    # Extract the integer values
    if not matches:
        return None

    if len(matches) == 1: # Will return the same value for both upper and lower
        return (int(matches[0]), int(matches[0]))
    
    if len(matches) > 1:
        return (int(matches[0]), int(matches[1]))
    
    return None

months = ["jan", "feb", "mar", "apr", "may", "jun", "jul", "aug", "sep", "oct", "nov", "dec"]
statuses = ["open", "prompt", "opn", "op", "pmt"]

def extract_month(s: str) -> Optional[int]:
    for i, month in enumerate(months, start=1):
        if month.lower() in s.lower():
            return i

    return None

class LocationForGeocoding(BaseModel):
    port: str = "" # Name of the port
    sea: str = "" # Name of the sea
    ocean: str = "" # Name of the ocean

    def clean(self):
        """Cleans up location object by 1. remove any leading/trailing spaces, 2. convert to lowercase. 3. remove punctuation."""
        self.port = re.sub(r'[^\w\s]', '', self.port.strip().lower())
        self.sea = re.sub(r'[^\w\s]', '', self.sea.strip().lower())
        self.ocean = re.sub(r'[^\w\s]', '', self.ocean.strip().lower())

